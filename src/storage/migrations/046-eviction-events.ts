  name: '046-eviction-events',
  sql: `
    CREATE TABLE eviction_events (
      id              TEXT PRIMARY KEY,
      parent_id       TEXT,
      substrate       TEXT NOT NULL
                        CHECK (substrate IN ('memory','policy','candidate','slot_item')),
      object_id       TEXT NOT NULL,
      object_scope    TEXT NOT NULL,
      from_state      TEXT NOT NULL
                        CHECK (from_state IN (
                          'proposed','active','shadow','quarantined',
                          'invalidated','evicted','purged'
                        )),
      to_state        TEXT NOT NULL
                        CHECK (to_state IN (
                          'proposed','active','shadow','quarantined',
                          'invalidated','evicted','purged'
                        )),
      trigger         TEXT NOT NULL,
      motivo          TEXT NOT NULL
                        CHECK (motivo IN (
                          'irrelevant','conflict','shift','low_roi',
                          'quota','expired','user_purge','security'
                        )),
      evidence_json   TEXT NOT NULL,
      outcome         TEXT NOT NULL
                        CHECK (outcome IN (
                          'applied',
                          'blocked_by_protection',
                          'blocked_by_hook',
                          'trigger_fired_no_action'
                        )),
      blocked_by      TEXT,
      actor           TEXT NOT NULL
                        CHECK (actor IN (
                          'loop_cold','compaction','user','hook','startup_probe'
                        )),
      session_id      TEXT REFERENCES sessions(id) ON DELETE SET NULL,
      dependents_json TEXT,
      recorded_at     INTEGER NOT NULL,
      purge_at        INTEGER
    );

    CREATE INDEX idx_evict_obj
      ON eviction_events(substrate, object_id);

    CREATE INDEX idx_evict_state
      ON eviction_events(to_state, recorded_at);

    CREATE INDEX idx_evict_purge
      ON eviction_events(purge_at)
      WHERE purge_at IS NOT NULL;
  `,
